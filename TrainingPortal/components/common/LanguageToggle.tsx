
import React from 'react';
import { useLanguage } from '../../contexts/LanguageContext';

const LanguageIcon = () => (
    <svg xmlns="http://www.w3.org/2000/svg" width="24" height="24" viewBox="0 0 24 24" fill="none" stroke="currentColor" strokeWidth="2" strokeLinecap="round" strokeLinejoin="round">
        <circle cx="12" cy="12" r="10"></circle>
        <line x1="2" y1="12" x2="22" y2="12"></line>
        <path d="M12 2a15.3 15.3 0 0 1 4 10 15.3 15.3 0 0 1-4 10 15.3 15.3 0 0 1-4-10 15.3 15.3 0 0 1 4-10z"></path>
    </svg>
);

const LanguageToggle: React.FC = () => {
  const { language, toggleLanguage } = useLanguage();

  return (
    <button
      onClick={toggleLanguage}
      className="p-2 rounded-full text-gray-500 dark:text-gray-400 hover:bg-gray-100 dark:hover:bg-gray-700 focus:outline-none focus:ring-2 focus:ring-offset-2 focus:ring-primary-500 relative"
      aria-label="Toggle language"
    >
      <LanguageIcon />
      <span className="absolute bottom-0 right-[-2px] block h-4 w-5 text-xs font-bold bg-gray-200 dark:bg-gray-600 rounded-sm leading-tight">
        {language.toUpperCase()}
      </span>
    </button>
  );
};

export default LanguageToggle;
